var c = 1
while(c <= 3){
    console.log(`Tudo bem? ${c}`)
    c++ // ou c = c + 1
}
/* Outra maneira de usar o 'while' no JS é:
'do'{
    faça alguma coisa
}'while'{
    enquanto isso for verdadeiro
} */
/* =-=-=EXEMPLO:=-=-= */

do{
    console.log(`Hello, world! ${c}`)
    c++ // ou c = c + 1
}while(c <= 7)